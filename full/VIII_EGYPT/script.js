function renderTeacherDirectory() {
    const grid = document.getElementById("teacher-grid");
    grid.innerHTML = "";

    const activeTeacherCodes = new Set();
    Object.values(scheduleData.schedule).forEach(dayList => {
        dayList.forEach(item => {
            if (item.kode) {
                item.kode.split('/').forEach(code => activeTeacherCodes.add(code.trim()));
            }
        });
    });

    Array.from(activeTeacherCodes).sort().forEach(code => {
        const fullName = scheduleData.teacher_map[code];
        if (!fullName) return;

        const card = document.createElement("div");
        card.className = "teacher-directory-card";
        
        const initials = fullName.split(' ').map(n => n[0]).filter(c => c === c.toUpperCase()).slice(0, 2).join('');

        card.innerHTML = `
            <div class="teacher-avatar-box">
                <span>${initials}</span>
            </div>
            <div class="teacher-details">
                <span class="teacher-fullname">${fullName}</span>
                <span class="teacher-code-badge">Kode: ${code}</span>
            </div>
        `;
        grid.appendChild(card);
    });
}

function setupPrintView() {
    const tbody = document.getElementById("print-table-body");
    tbody.innerHTML = "";

    const timeSlotsSet = new Set();
    Object.values(scheduleData.schedule).forEach(dayEvents => {
        dayEvents.forEach(e => {
            timeSlotsSet.add(e.waktu);
        });
    });

    const timeSlots = Array.from(timeSlotsSet).sort((a, b) => {
        const timeA = a.split('-')[0].trim();
        const timeB = b.split('-')[0].trim();
        return timeA.localeCompare(timeB);
    });

    timeSlots.forEach(slot => {
        const row = document.createElement("tr");
        
        let rowHtml = "<td><strong>" + slot + "</strong></td>";
        
        const days = ["SENIN", "SELASA", "RABU", "KAMIS", "JUMAT", "SABTU"];
        days.forEach(day => {
            const events = scheduleData.schedule[day] ? scheduleData.schedule[day].filter(e => e.waktu === slot) : [];
            if (events.length > 0) {
                const items = events.map(e => {
                    const teacherName = e.kode ? getTeacherFullName(e.kode) : "";
                    return "<div><strong>" + e.subject + "</strong>" + (teacherName ? "<br><small>" + e.kode + "</small>" : "") + "</div>";
                }).join('');
                rowHtml += "<td>" + items + "</td>";
            } else {
                rowHtml += "<td>-</td>";
            }
        });
        
        row.innerHTML = rowHtml;
        tbody.appendChild(row);
    });
}
