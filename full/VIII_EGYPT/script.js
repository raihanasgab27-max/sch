function getSubjectCategory(subject) {
    const s = subject.toUpperCase();
    if (s.includes("MATEMATIKA")) return "cat-math";
    if (s.includes("IPA") || s.includes("INFORMATIKA")) return "cat-science";
    if (s.includes("B. INDO") || s.includes("CONVERSATION") || s.includes("GRAMMAR") || s.includes("B. ARAB")) return "cat-language";
    if (s.includes("TAHFIDZ") || s.includes("TAJWID") || s.includes("FIQIH") || s.includes("PAI")) return "cat-religion";
    if (s.includes("IPS") || s.includes("PKN")) return "cat-social";
    if (s.includes("PJOK")) return "cat-other";
    if (s.includes("ISTIRAHAT") || s.includes("ISHOMA")) return "cat-general";
    return "cat-general";
}

